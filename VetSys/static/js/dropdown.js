
function petsDropdown() {
    document.getElementById('pets_dropdown').style.display = "none";
    document.getElementById('pets_dropclose').style.display = "block";
    document.getElementById('pdb').style.display = "block";
}

function petsDropclose() {
    document.getElementById('pets_dropdown').style.display = "block";
    document.getElementById('pets_dropclose').style.display = "none";
    document.getElementById('pdb').style.display = "none";
}

